    {
        
        //get the user who is canceling
        $user = $this->user->findOrFail($id);

        $user->delete();

        // Make announcement, and send the $user object
        Event::fire('cancellation', ['user' => $user]);

        // Redirect to home page
        return Redirect::home();
    }
